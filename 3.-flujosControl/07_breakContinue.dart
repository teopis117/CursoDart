main()
{

for( var i=0;i<10;i++)
{
  if(i==5)
  {
    //que hace el continue: salta hacia la siguiente iteracion y  ya no hace el siguiente codigo que tenga delante de el
    continue;
  }

  if(i==8)
  {
    //con break terminamos el ciclo y termina  y es muy util para busquedas y listados
    break;
  }


  print(i);
}


//recordemos continue=seguir pero saltar break=terminar el ciclo 
//


}